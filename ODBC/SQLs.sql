SELECT ECL.LODTE,ECL.LORD,ECH.HCPO,ECL.LLINE,ECL.LPROD,IIM.IDESC,ECL.LQORD,ECL.LQALL,ECL.LQSHP,ECL.LNET,ECL.LRDTE,ECL.LSDTE,ECL.LLOC,IIM.IVEND,AVM.VNDNAM
FROM ECL ECL
INNER JOIN ECHL01 ECH ON ECL.LORD=ECH.HORD
INNER JOIN IIML01 IIM ON ECL.LPROD=IIM.IPROD
INNER JOIN AVML01 AVM ON IIM.IVEND=AVM.VENDOR
WHERE ECH.HCUST=9125
AND ECL.LSTAT = 'E'
AND ECH.HEDTE > 20231101
AND ECL.LQORD > 0