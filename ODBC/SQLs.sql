FROM IIML01 A
LEFT JOIN ICXL01 B
ON A.ICLAS = B.CXCLAS

JPNPOBYRT

SELECT HPO.PVEND,AVM.VNDNAM,HPO.PEDTE,HPO.PORD,HPO.PLINE,IIM.IPROD,IIM.IDESC,HPO.PQORD,HPO.PECST,HPO.POCUR,HPO.PDDTE,HPO.HVDUE,HPO.PCMT,IIM.ILOC,IWI.WLEAD,IWI.WLOTS,IWI.WILOC
FROM HPOL01 HPO
INNER JOIN IIML01 IIM
ON HPO.PPROD = IIM.IPROD
INNER JOIN AVML01 AVM
ON HPO.PVEND=AVM.VENDOR
LEFT JOIN IWIL01 IWI
ON IIM.IPROD=IWI.WPROD
WHERE HPO.PEDTE > 20231101
AND HPO.PWHSE IN ('A1','5','6')
AND IIM.IPROD NOT LIKE 'FA%'
AND HPO.PVEND > 10000
AND HPO.PCMT !='TO BE DELETED'
AND IWI.WWHS = '5'

