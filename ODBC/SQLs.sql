SELECT WPROD, WMIN, WWHS, SUM(WOPB + WRCT - WISS + WADJ) AS STKOH 
FROM IWIL01 
WHERE WPROD NOT LIKE 'FA%' 
  AND WWHS = '5' 
GROUP BY WPROD, WMIN, WWHS
HAVING SUM(WOPB + WRCT - WISS + WADJ) > 0


